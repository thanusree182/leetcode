class Solution
{
    public:
    //Function to delete middle element of a stack.
    void deleteMid(stack<int>&s, int sizeOfStack)
    {
        // code here.. 
        int mid=(sizeOfStack)/2;
        stack<int> h;
        while(mid>0){
            int top=s.top();
            s.pop();
            h.push(top);
            mid--;
        }
        s.pop();
        while(!h.empty()){
            int a=h.top();
            h.pop();
            s.push(a);
        }
    }
};

